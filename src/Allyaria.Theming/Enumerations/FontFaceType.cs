namespace Allyaria.Theming.Enumerations;

/// <summary>Defines the font face classifications used within the Allyaria theming system.</summary>
public enum FontFaceType
{
    /// <summary>
    /// Represents a monospaced font, where each character occupies the same horizontal width. Commonly used for code blocks,
    /// tabular data, and technical text.
    /// </summary>
    Monospace,

    /// <summary>
    /// Represents a sans-serif font, characterized by clean, modern strokes without decorative endings. Typically used for
    /// general UI text and body content for optimal readability on screens.
    /// </summary>
    SansSerif,

    /// <summary>
    /// Represents a serif font, featuring small decorative strokes at the ends of letters. Commonly used for headings,
    /// branding, or formal text presentation.
    /// </summary>
    Serif
}
