namespace Allyaria.Theming.Constants;

/// <summary>
/// Provides Material Design–compliant sizing constants based on a 4px/8px grid. These values can be used for consistent
/// margins, paddings, and component dimensions.
/// </summary>
[ExcludeFromCodeCoverage(Justification = "This class is a library of constant values.")]
public static class Sizing
{
    /// <summary>100% - full size.</summary>
    public static readonly string Full = "100%";

    /// <summary>50% - half size.</summary>
    public static readonly string Half = "50%";

    /// <summary>25% - quarter size.</summary>
    public static readonly string Quarter = "25%";

    /// <summary>1rem - Same relative size.</summary>
    public static readonly string Relative = "1rem";

    /// <summary>1.25rem - Large relative size.</summary>
    public static readonly string RelativeLarge1 = "1.25rem";

    /// <summary>1.5rem - Larger relative size.</summary>
    public static readonly string RelativeLarge2 = "1.5rem";

    /// <summary>1.75rem - Extra Large relative size.</summary>
    public static readonly string RelativeLarge3 = "1.75rem";

    /// <summary>2rem - Largest relative size.</summary>
    public static readonly string RelativeLarge4 = "2rem";

    /// <summary>0.875rem - Small relative size.</summary>
    public static readonly string RelativeSmall1 = "0.875rem";

    /// <summary>0.75rem - Smaller relative size.</summary>
    public static readonly string RelativeSmall2 = "0.75rem";

    /// <summary>0.625rem - Extra small relative size.</summary>
    public static readonly string RelativeSmall3 = "0.625rem";

    /// <summary>0.5rem - Smallest relative size.</summary>
    public static readonly string RelativeSmall4 = "0.5rem";

    /// <summary>0px — no size.</summary>
    public static readonly string Size0 = "0px";

    /// <summary>4px — micro spacing, used sparingly for fine alignment.</summary>
    public static readonly string Size1 = "4px";

    /// <summary>72px — extra large spacing step.</summary>
    public static readonly string Size10 = "72px";

    /// <summary>80px — extra large spacing step, upper bound of the default scale.</summary>
    public static readonly string Size11 = "80px";

    /// <summary>8px — base spacing unit (1 step on Material grid).</summary>
    public static readonly string Size2 = "8px";

    /// <summary>16px — default internal padding for many components.</summary>
    public static readonly string Size3 = "16px";

    /// <summary>24px — common margin/gutter size on larger layouts.</summary>
    public static readonly string Size4 = "24px";

    /// <summary>32px — larger spacing for layout separation.</summary>
    public static readonly string Size5 = "32px";

    /// <summary>40px — large step in the spacing scale.</summary>
    public static readonly string Size6 = "40px";

    /// <summary>48px — minimum touch target size per accessibility guidance.</summary>
    public static readonly string Size7 = "48px";

    /// <summary>56px — commonly used for component heights (e.g., toolbars).</summary>
    public static readonly string Size8 = "56px";

    /// <summary>64px — extra large spacing step.</summary>
    public static readonly string Size9 = "64px";

    /// <summary>2px — double pixel spacing, usually used for borders.</summary>
    public static readonly string Thick = "2px";

    /// <summary>1px — single pixel spacing, usually used for borders.</summary>
    public static readonly string Thin = "1px";

    /// <summary>75% - three quarter size.</summary>
    public static readonly string ThreeQuarter = "75%";
}
